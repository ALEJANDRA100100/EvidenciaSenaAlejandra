def ElevarAlCubo(numero):
    return numero**3
resultado = ElevarAlCubo(3)
print (resultado) 


def sumar(num1, num2):
    return num1 + num2
resultado = sumar(5, 7)
print (resultado)

def restar(num1, num2):
    return num1 - num2      
resultado = restar(10, 4)
print (resultado)

def multiplicar(num1, num2):
    return num1 * num2      
resultado = multiplicar(6, 8)
print (resultado)

def dividir(num1, num2):
    return num1 / num2      
resultado = dividir(20, 5)
print (resultado)   


def potencia(base, exponente):
    return base ** exponente
resultado = potencia(2, 4)
